	local e?=Effect.CreateEffect(c)
	e?:SetDescription(aux.Stringid(id,yyy))
	e?:SetCategory(CATEGORY_)
	e?:SetType(EFFECT_TYPE_IGNITION)
	e?:SetRange(LOCATION_)
	e?:SetProperty(EFFECT_FLAG_)
	e?:SetCountLimit(1,id)
	e?:SetCondition(s.)
	e?:SetCost(s.)
	e?:SetTarget(s.)
	e?:SetOperation(s.)
	c:RegisterEffect(e?)
	
